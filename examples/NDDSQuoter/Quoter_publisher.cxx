/* Delete all entities */
static int publisher_shutdown(
    DDSDomainParticipant *participant)
{
    DDS_ReturnCode_t retcode;
    int status = 0;

    if (participant != NULL) {
        retcode = participant->delete_contained_entities();
        if (retcode != DDS_RETCODE_OK) {
            printf("delete_contained_entities error %d\n", retcode);
            status = -1;
        }

        retcode = DDSTheParticipantFactory->delete_participant(participant);
        if (retcode != DDS_RETCODE_OK) {
            printf("delete_participant error %d\n", retcode);
            status = -1;
        }
    }

    /* RTI Data Distribution Service provides finalize_instance() method for
       people who want to release memory used by the participant factory
       singleton. Uncomment the following block of code for clean destruction of
       the participant factory singleton. */
/*
    retcode = DDSDomainParticipantFactory::finalize_instance();
    if (retcode != DDS_RETCODE_OK) {
        printf("finalize_instance error %d\n", retcode);
        status = -1;
    }
*/

    return status;
}

extern "C" int publisher_main(int domainId, int sample_count)
{
    DDSDomainParticipant *participant = NULL;
    DDSPublisher *publisher = NULL;
    DDSTopic *topic = NULL;
    DDSDataWriter *writer = NULL;
    QuoterDataWriter * Quoter_writer = NULL;
    DDS_ReturnCode_t retcode;
    const char *type_name = NULL;
    int count = 0;  
    struct DDS_Duration_t send_period = {4,0};

    /* To customize participant QoS, use 
       DDSTheParticipantFactory->get_default_participant_qos()
       instead */
    participant = DDSTheParticipantFactory->create_participant(
        domainId, DDS_PARTICIPANT_QOS_DEFAULT, 
        NULL /* listener */, DDS_STATUS_MASK_NONE);
    if (participant == NULL) {
        printf("create_participant error\n");
        publisher_shutdown(participant);
        return -1;
    }

    /* To customize publisher QoS, use
       participant->get_default_publisher_qos() instead */
    publisher = participant->create_publisher(
        DDS_PUBLISHER_QOS_DEFAULT, NULL /* listener */, DDS_STATUS_MASK_NONE);
    if (publisher == NULL) {
        printf("create_participant error\n");
        publisher_shutdown(participant);
        return -1;
    }

    /* Register type before creating topic */
    type_name = QuoterTypeSupport::get_type_name();
    retcode = QuoterTypeSupport::register_type(
        participant, type_name);
    if (retcode != DDS_RETCODE_OK) {
        printf("register_type error %d\n", retcode);
        publisher_shutdown(participant);
        return -1;
    }

    /* To customize topic QoS, use
       participant->get_default_topic_qos() instead */
    topic = participant->create_topic(
        "Example Quoter",
        type_name, DDS_TOPIC_QOS_DEFAULT, NULL /* listener */,
        DDS_STATUS_MASK_NONE);
    if (topic == NULL) {
        printf("create_topic error\n");
        publisher_shutdown(participant);
        return -1;
    }

    /* To customize data writer QoS, use
       publisher->get_default_datawriter_qos() instead */
    writer = publisher->create_datawriter(
        topic, DDS_DATAWRITER_QOS_DEFAULT, NULL /* listener */,
        DDS_STATUS_MASK_NONE);
    if (writer == NULL) {
        printf("create_datawriter error\n");
        publisher_shutdown(participant);
        return -1;
    }
    Quoter_writer = QuoterDataWriter::narrow(writer);
    if (Quoter_writer == NULL) {
        printf("DataWriter narrow error\n");
        publisher_shutdown(participant);
        return -1;
    }

    // Create IBM data sample for writing
    Quoter *ibmInstance = QuoterTypeSupport::create_data();
    if (ibmInstance == NULL) {
        std::cerr << "QuoterTypeSupport::create_data error" << std::endl;
        publisher_shutdown(participant);
        return -1;
    }

    // Set constant information for the IBM stock
    std::string ibmSymbol("IBM");
    ::strcpy(ibmInstance->symbol, ibmSymbol.c_str());
    std::string ibmName("International Business Machines");
    ::strcpy(ibmInstance->full_name, ibmName.c_str());

    // Create Microsoft data sample for writing
    Quoter *msftInstance = QuoterTypeSupport::create_data();
    if (msftInstance == NULL) {
        std::cerr << "QuoterTypeSupport::create_data error" << std::endl;
        publisher_shutdown(participant);
        return -1;
    }

    // Set constant information for the Microsoft stock
    std::string msftSymbol("MSFT");
    ::strcpy(msftInstance->symbol, msftSymbol.c_str());
    std::string msftName("Microsoft Corporation");
    ::strcpy(msftInstance->full_name, msftName.c_str());

    /* For data type that has key, if the same instance is going to be
       written multiple times, initialize the key here
       and register the keyed instance prior to writing */
    DDS_InstanceHandle_t instance_handle = DDS_HANDLE_NIL;
/*
    instance_handle = Quoter_writer->register_instance(*instance);
*/

    /* Main loop */
    for (count=0; (sample_count == 0) || (count < sample_count); ++count) {

        printf("Writing Quoter, count %d\n", count);

        /* Modify the data to be sent here */
        ibmInstance->price = count * 100;

        retcode = Quoter_writer->write(*ibmInstance, instance_handle);
        if (retcode != DDS_RETCODE_OK) {
            printf("write error %d\n", retcode);
        }

        msftInstance->price = count * 10;

        retcode = Quoter_writer->write(*msftInstance, instance_handle);
        if (retcode != DDS_RETCODE_OK) {
            printf("write error %d\n", retcode);
        }

        NDDSUtility::sleep(send_period);
    }

/*
    retcode = Quoter_writer->unregister_instance(
        *instance, instance_handle);
    if (retcode != DDS_RETCODE_OK) {
        printf("unregister instance error %d\n", retcode);
    }
*/

    // Delete IBM data sample
    retcode = QuoterTypeSupport::delete_data(ibmInstance);
    if (retcode != DDS_RETCODE_OK) {
       std::cerr << "QuoterTypeSupport::delete_data error "
                 << retcode << std::endl;
    }

    // Delete Microsoft data sample
    retcode = QuoterTypeSupport::delete_data(msftInstance);
    if (retcode != DDS_RETCODE_OK) {
       std::cerr << "QuoterTypeSupport::delete_data error "
                 << retcode << std::endl;
    }

    /* Delete all entities */
    return publisher_shutdown(participant);
}

